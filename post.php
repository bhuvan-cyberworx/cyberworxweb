              <h3 class="lead" style="font-size: 30px;color: #02c9c8;padding:30px;">6) Cloud Based Application Development</h3>

                <!-- <picture class=""> <img src="uploads/CLOUD.jpg" alt=""> </picture><br><br> -->

           			
			<p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">These apps don’t take as much space as regular apps on the users’ device without compromising with the user experience and keeping the same data across all devices. </p>
			
			<p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">Developing cloud-based apps can be less complicated than one version of the app. The user does not have to regularly update the app from the app store and a universal user experience and features can be provided to all users at the same time. This trend seems to be continuing to grow in 2017 and will keep on growing in the future.</p><br>
			
              <h3 class="lead" style="font-size: 30px;color: #02c9c8;padding:30px;">7) Greater Attention to Mobile App Security and Privacy </h3>
			 
     <!-- <picture class=""> <img src="uploads/Anonymous.jpg" style="width:80%; alt=" "=""> </picture><br><br> -->
			 
<p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">With Apps collecting data about the users more than ever before, risks, vulnerabilities and security breaches are getting bigger than ever before. This is a great concern for app developers, and mobile app business owners who are worried about their apps’ performance and user data.</p>
			 
			 <p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">Tech companies are introducing Biometrics like Fingerprint and Iris Scanning with an ability to encrypt data locally in the user’s phone for ensuring greater security. Hence, we must expect more such technologies upcoming int he future to make our data much more secure.</p><br>

<h3 class="lead" style="font-size: 30px;color: #02c9c8;padding:30px;">8) Wearable Technology influencing Mobile App Development</h3>
			 
			 <!-- <picture class=""> <img src="uploads/APPLEWATCH.jpg" style="width:80%; alt=" "=""> </picture><br><br> -->
			 
			 			 
			 <p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">According to International Data Corporation, the market for wearable devices is going to experience a composite annual growth rate of 20.3%, leading to 213.6 million units shipped in 2020. Market is flooded with amazing wearable devices like Apple Watch, Fitbit, Mi Bands, LG Urbane, Samsung Gear, etc.</p>
			 
			 <p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">Wearables are semi-independent and are used as a phone companion. Hence, they function on the basis of apps installed on the phone. Many dedicated apps are already available for the wearable devices but the app selection is not as wide as that of mobile phones. With the growth in Wearables sector, we are pretty sure that by 2020, apps available for wearable will be as big as those available for mobile phones.</p><br><br>
			 

