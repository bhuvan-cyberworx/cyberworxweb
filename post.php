			<br>
            <p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">In April 1973, <a href="https://www.britannica.com/biography/Martin-Cooper">Marty Cooper</a> - Motorola Engineer made the first call from a real handheld portable cell phone. This phone- <a href="https://en.wikipedia.org/wiki/Motorola_DynaTAC">Motorla DynaTAC </a>had an LED screen and boasted 30 minutes of talk time with eight hours of standby. It was priced at almost $4,000 in the early ’80s — no wonders it made its first appearance in the hands of bankers on Wall Street!</p>
			
			
			
           
			<p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">Mobile Phones today are as powerful as PCs. Infact according to the data released by StatCounter, In October 2016, more users last month accessed the web from mobile devices like smart phones and tablets than they did from desktops or notebooks.</p>
			
			<p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">When Apple launched iPhone back in 2007, it completely revolutionized the way we use mobile phones. With never seen before features like a Multi Touch Capacitive touch screen, Apple also introduced its very own App Store from which mobile users can download tons of Applications and Games which gave unlimited capabilities to the iPhone.</p>
			
			<p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">With plenty of competitors in the market fighting for their share of the pie, it was a dawn of Apps and Games Industry. Developers started to earn handsomely for the amazing apps and games they created and users loved them on their devices. Companies like Apple, Google, Microsoft, Nokia, Blackberry, etc had their own dedicated store and charged commissions to feature apps and games. It was a win-win situation for everyone.</p>
			
			<p style="font-size: 16px;color: #fff;padding: 0px 30px;text-align: left;">Fast forward it to 2017; we cannot imagine our lives without Mobile Apps. Booking a Cab? You use Uber. Ordering a Pizza? You use Dominos. From a Pin to a Private Jet, You have an app for it.
So the question is, whats next?
</p>

